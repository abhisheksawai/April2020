package basicsOfJava;

public class UnaryOpe4 {

	
	public static void main(String[] args) {

		int num1 = 10;
		int num2 = 20;
		
		num1--;  //9
		System.out.println(num1); //9
		
		System.out.println(num2--); //first print 20 and then it become 19
		System.out.println(num2);  //19 here
		
	//	System.out.println(num++ + num++);
		
		
	}

}
